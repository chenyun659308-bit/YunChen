export function useI18n() {
  const t = (key) => (locale.value === 'en' ? en : zh)[key] || key
  function l(item, field) {
    if (item && field && locale.value === 'en' && item[field + '_en']) return item[field + '_en']
    return item ? item[field] : ''
  }
  const toggleLang = () => { locale.value = locale.value === 'zh' ? 'en' : 'zh' }
  return { locale, t, toggleLang, l }
}
